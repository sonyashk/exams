# Этот скрипт принимает два аргумента командной строки: директорию и размер файла в байтах. Затем он использует команду find для обхода всех файлов в указанной директории и ее поддиректориях, фильтруя файлы по размеру. В итоге выводятся на экран имена файлов, размер которых равен указанному.
#!/bin/bash

# Проверяем, переданы ли аргументы - директория и размер файла
if [ $# -ne 2 ]; then
    echo "Usage: $0 <directory> <size>"
    exit 1
fi

# Проверяем, является ли первый аргумент директорией
if [ ! -d "$1" ]; then
    echo "Error: '$1' is not a directory."
    exit 1
fi

# Получаем абсолютный путь к директории
directory=$(realpath "$1")

# Получаем размер файла из второго аргумента
size=$2

# Обходим все файлы в директории и ее поддиректориях
find "$directory" -type f -size "${size}c" | while IFS= read -r file; do
    # Выводим имя файла
    echo "$file"
done
